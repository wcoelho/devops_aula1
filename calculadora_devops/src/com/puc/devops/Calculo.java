package com.puc.devops;

public class Calculo {

    public static double subtrair(double a, double b) {
        System.out.println("Subtraindo: " + a + "-" + b);
        return (a - b);
    }

    public static double somar(double a, double b) {
        System.out.println("Somando: " + a + "+" + b);
        return (a + b);
    }
}